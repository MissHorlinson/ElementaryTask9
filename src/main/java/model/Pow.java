package model;

public class Pow {
    private int number;
    private int pow;

    public Pow(int number, int pow) {
        this.number = number;
        this.pow = pow;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getPow() {
        return pow;
    }

    public void setPow(int pow) {
        this.pow = pow;
    }
}
